def _requirements_mention_validate(requirements: list[str]) -> bool:
    """Return True when requirements explicitly call for validate()."""
    lowered = " ".join(requirement.lower() for requirement in requirements)
    return "validate()" in lowered or "must include validate" in lowered


def _requirements_mention_status(requirements: list[str]) -> bool:
    """Return True when requirements mention status workflow expectations."""
    lowered = " ".join(requirement.lower() for requirement in requirements)
    return "status" in lowered


def _requirements_mention_classify(requirements: list[str]) -> bool:
    """Return True when requirements mention classify()."""
    lowered = " ".join(requirement.lower() for requirement in requirements)
    return "classify(" in lowered or "must expose classify" in lowered
